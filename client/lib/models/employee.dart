class Employee {
  final String name;
  final String id;
  final String shift;
  final String role;
  final String department;
  final String joined;
  final String manager;
  final String email;

  Employee({
    required this.name,
    required this.id,
    required this.shift,
    required this.role,
    required this.department,
    required this.joined,
    required this.manager,
    required this.email,
  });
}
