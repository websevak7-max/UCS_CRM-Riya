class MonthlyBreakdown {
  final String month;
  final int present;
  final int absent;
  final int late;
  final int leave;
  final int rate;

  MonthlyBreakdown({
    required this.month,
    required this.present,
    required this.absent,
    required this.late,
    required this.leave,
    required this.rate,
  });
}
