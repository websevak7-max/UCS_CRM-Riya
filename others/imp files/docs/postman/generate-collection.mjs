const base = '{{base_url}}'
const auth = [{ key: 'Authorization', value: 'Bearer {{token}}' }]
const json = [{ key: 'Content-Type', value: 'application/json' }]

function req(method, path, desc, body, headers = [...json, ...auth], query) {
  const url = { raw: `${base}${path}`, host: [base], path: path.split('/').filter(Boolean) }
  if (query) url.query = query
  const r = { method, header: headers, url, description: desc }
  if (body) r.body = { mode: 'raw', raw: JSON.stringify(body, null, 2) }
  return { name: desc || path, request: r }
}

function folder(name, items) {
  return { name, item: items }
}

